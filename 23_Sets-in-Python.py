s = {2, 3, 2, 2, 1, 1, 32, 23, 2, 19}
print(s)
print(type(s))
'''
OUTPUT

{32, 1, 2, 3, 19, 23}
<class 'set'>
'''

s = {"aman", ",aman", "aman", 2, 33, 1, 19, 1, True}
print(s)
s = {}
print(s)
print(type(s))
s = set()
print(s)
print(type(s))
'''
OUTPUT

{33, 2, 1, 'aman', 19, ',aman'}
{}
<class 'dict'>
set()
<class 'set'>
'''
